@dataclass(frozen=True)
class ObsObject:
    """Object of Obs: fixed Krein ambient + a chosen observable menu M."""

    name: str
    n: int
    menu: tuple                      # generators of the *-algebra M (= <M_R>)
    line_from: np.ndarray            # separating datum d : source line (ghost)
    line_to: np.ndarray              # separating datum d : target line (physical)
    parity: np.ndarray               # ghost-parity selector P (ambient datum)

    def algebra_basis(self):
        return generated_algebra_basis(self.menu, self.n)


@dataclass(frozen=True)
class ObsMorphism:
    """Menu-enlargement inclusion M(source) subset M(target)."""

    name: str
    source: ObsObject
    target: ObsObject


@dataclass(frozen=True)
class AdmObject:
    """Object of Adm: an admissibility structure."""

    grading: str                     # "graded" | "ungraded"
    verdict: str                     # "FORCED" (E3) | "DECLARED" (E0)
    witness: dict = field(default_factory=dict)


VERDICT_RANK = {"FORCED": 0, "DECLARED": 1}  # enlarging only ever un-forces


@dataclass(frozen=True)
class AdmMorphism:
    """Verdict-monotone refinement: FORCED -> DECLARED allowed, reverse forbidden."""

    source: AdmObject
    target: AdmObject

    @property
    def monotone(self) -> bool:
        return VERDICT_RANK[self.target.verdict] >= VERDICT_RANK[self.source.verdict]


@runtime_checkable
class VerdictFunctor(Protocol):
    """The ONE signature both functors instantiate: Obs -> Adm."""

    def on_object(self, obj: ObsObject) -> AdmObject: ...

    def on_morphism(self, mor: ObsMorphism) -> AdmMorphism: ...


# ---------------------------------------------------------------------------
# F_TaF -- operational-recoverability functor (ZERO physics input)
# ---------------------------------------------------------------------------

class FTaF:
    """menu |-> (invariant decomposition ; DECLARED iff datum recoverable).

    Consumes ONLY: the menu algebra and the two given lines. Never references
    K_FORM, the metric, ghost-parity SEMANTICS, or Mostafazadeh. (The parity
    matrix, if present in a menu, is treated as a bare operator generating an
    algebra -- its Krein meaning is never used here.)
    """

    reads_physics = False

    def on_object(self, obj: ObsObject) -> AdmObject:
        basis = obj.algebra_basis()
        recoverable = recovery_exists(basis, obj.line_from, obj.line_to, obj.n)
        reducible = commutant_dimension(obj.menu, obj.n) > 1
        verdict = "DECLARED" if recoverable else "FORCED"
        grading = "graded" if reducible else "ungraded"
        return AdmObject(
            grading=grading,
            verdict=verdict,
            witness={
                "reading": "TaF-operational",
                "datum_recoverable": bool(recoverable),
                "algebra_dim": len(basis),
                "invariant_decomposition": reducible,
            },
        )

    def on_morphism(self, mor: ObsMorphism) -> AdmMorphism:
        return AdmMorphism(self.on_object(mor.source), self.on_object(mor.target))


# ---------------------------------------------------------------------------
# F_phys -- Mostafazadeh metric-selection functor (ZERO TaF input)
# ---------------------------------------------------------------------------

class FPhys:
    """menu |-> (Krein grading ; DECLARED iff a unique definite metric is pinned).

    Consumes ONLY: metric selection (pseudo-Hermiticity intertwiners). Never
    references recoverability, the separating-datum lines, or any TaF verdict.
    """

    reads_taf = False

    def on_object(self, obj: ObsObject) -> AdmObject:
        dim, _ = metric_space(obj.menu, obj.n)
        unique_definite, has_indef = metric_is_uniquely_definite(obj.menu, obj.n)
        # Irreducible family pins a definite metric -> ghosts merged -> no grading.
        if unique_definite and not has_indef:
            verdict, grading = "DECLARED", "ungraded"
        else:
            verdict, grading = "FORCED", "graded"
        return AdmObject(
            grading=grading,
            verdict=verdict,
            witness={
                "reading": "physics-metric",
                "metric_space_dim": dim,
                "unique_definite_metric": bool(unique_definite),
                "indefinite_metric_survives": bool(has_indef),
            },
        )

    def on_morphism(self, mor: ObsMorphism) -> AdmMorphism:
        return AdmMorphism(self.on_object(mor.source), self.on_object(mor.target))


# ---------------------------------------------------------------------------
# The adapter: natural transformation eta : F_TaF => F_phys
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class EtaComponent:
    """eta_M : F_TaF(M) -> F_phys(M).  An Adm-iso iff both agree on (grading,
    verdict); the witness correspondence recoverable <=> definite-metric is the
    load-bearing content (K5 guard: content in the witness, not the bare verdict)."""

    taf: AdmObject
    phys: AdmObject

    @property
    def is_iso(self) -> bool:
        return self.taf.grading == self.phys.grading and self.taf.verdict == self.phys.verdict


def eta_component(obj: ObsObject) -> EtaComponent:
    return EtaComponent(FTaF().on_object(obj), FPhys().on_object(obj))


def naturality_square_commutes(mor: ObsMorphism) -> bool:
    """Square over the enlargement arrow commutes: both functors send it to the
    SAME Adm morphism and the eta components at both ends are isos."""
    f_taf, f_phys = FTaF(), FPhys()
    src = eta_component(mor.source)
    tgt = eta_component(mor.target)
    if not (src.is_iso and tgt.is_iso):
        return False
    taf_mor = f_taf.on_morphism(mor)
    phys_mor = f_phys.on_morphism(mor)
    # eta components are identities on equal Adm objects, so commuting reduces to
    # the two legs being the same Adm morphism (same endpoints, same direction).
    same_ends = (
        taf_mor.source.verdict == phys_mor.source.verdict
        and taf_mor.target.verdict == phys_mor.target.verdict
        and taf_mor.source.grading == phys_mor.source.grading
        and taf_mor.target.grading == phys_mor.target.grading
    )
    return same_ends and taf_mor.monotone and phys_mor.monotone


