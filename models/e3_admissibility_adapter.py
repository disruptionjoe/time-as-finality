I2 = np.eye(2, dtype=complex)
K_FORM = np.array([[0, 1], [1, 0]], dtype=complex)          # indefinite Krein form
P_PARITY = np.array([[0, 1], [1, 0]], dtype=complex)        # ghost-parity selector (= K here)
Z_GRADE = np.array([[1, 0], [0, -1]], dtype=complex)        # grading-breaking generator
# Null basis u = e1, v = e2 :  <u,v>_K = 1, <u,u>_K = <v,v>_K = 0
U_NULL = np.array([1, 0], dtype=complex)
V_NULL = np.array([0, 1], dtype=complex)
# P eigenlines: (u+v) K-norm +2 (physical, P-even); (u-v) K-norm -2 (ghost, P-odd)
LINE_PHYS = np.array([1, 1], dtype=complex)                 # separating datum: target line
LINE_GHOST = np.array([1, -1], dtype=complex)               # separating datum: source line
# Grading-preserving unitary (diag(2,3) in the P-eigenbasis) = 2.5 I - 0.5 P
U_PRESERVE = 2.5 * I2 - 0.5 * P_PARITY


def dagger(m: np.ndarray) -> np.ndarray:
    return m.conj().T


def k_norm(x: np.ndarray, k: np.ndarray = K_FORM) -> complex:
    return complex((dagger(x.reshape(-1, 1)) @ k @ x.reshape(-1, 1))[0, 0])


