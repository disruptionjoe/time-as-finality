def build_objects():
    """M0 (reducible <I,P>), M_all (irreducible <P,Z>), M1 (grading-preserving)."""
    m0 = ObsObject(
        name="M0 = <I, P>",
        n=2,
        menu=(I2, P_PARITY),
        line_from=LINE_GHOST,
        line_to=LINE_PHYS,
        parity=P_PARITY,
    )
    m_all = ObsObject(
        name="M_all = <P, Z> = M2(C)",
        n=2,
        menu=(P_PARITY, Z_GRADE),
        line_from=LINE_GHOST,
        line_to=LINE_PHYS,
        parity=P_PARITY,
    )
    m1 = ObsObject(
        name="M1 = <I, P, U> (grading-preserving U)",
        n=2,
        menu=(I2, P_PARITY, U_PRESERVE),
        line_from=LINE_GHOST,
        line_to=LINE_PHYS,
        parity=P_PARITY,
    )
    return m0, m_all, m1


def run_adapter():
    """Run the full adapter on the toy and return a structured result dict."""
    m0, m_all, m1 = build_objects()
    arrow = ObsMorphism("M0 subset M_all", m0, m_all)
    arrow_pre = ObsMorphism("M0 subset M1", m0, m1)

    f_taf, f_phys = FTaF(), FPhys()

    def snapshot(obj):
        a_taf = f_taf.on_object(obj)
        a_phys = f_phys.on_object(obj)
        return {
            "name": obj.name,
            "P_central": is_central(obj.parity, obj.menu, obj.n),
            "commutant_dim": commutant_dimension(obj.menu, obj.n),
            "metric_space_dim": metric_space(obj.menu, obj.n)[0],
            "F_TaF": (a_taf.grading, a_taf.verdict),
            "F_phys": (a_phys.grading, a_phys.verdict),
            "eta_iso": eta_component(obj).is_iso,
            "taf_witness": a_taf.witness,
            "phys_witness": a_phys.witness,
        }

    return {
        "objects": {
            "M0": snapshot(m0),
            "M_all": snapshot(m_all),
            "M1": snapshot(m1),
        },
        "naturality_M0_to_Mall": naturality_square_commutes(arrow),
        "naturality_M0_to_M1": naturality_square_commutes(arrow_pre),
        "both_functors_typecheck": (
            isinstance(f_taf, VerdictFunctor) and isinstance(f_phys, VerdictFunctor)
        ),
        "shared_knob": "centrality of ghost-parity selector P in the observable algebra",
    }


