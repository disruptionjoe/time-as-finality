def generated_algebra_basis(gens, n: int):
    """Linear basis of the unital *-algebra generated by ``gens`` (n x n)."""
    seed = [np.eye(n, dtype=complex)]
    for g in gens:
        seed.append(np.array(g, dtype=complex))
        seed.append(dagger(np.array(g, dtype=complex)))

    basis_vecs: list[np.ndarray] = []

    def add(mat: np.ndarray) -> bool:
        v = mat.reshape(-1)
        if basis_vecs:
            a = np.array(basis_vecs).T
            coef, *_ = np.linalg.lstsq(a, v, rcond=None)
            if np.linalg.norm(a @ coef - v) < TOL:
                return False
        basis_vecs.append(v)
        return True

    for m in seed:
        add(m)

    changed = True
    while changed:
        changed = False
        current = [bv.reshape(n, n) for bv in basis_vecs]
        for a_mat in current:
            for b_mat in seed:
                if add(a_mat @ b_mat):
                    changed = True
    return [bv.reshape(n, n) for bv in basis_vecs]


def commutant_dimension(gens, n: int) -> int:
    """dim { X : [g, X] = 0 for all generators g }."""
    rows = []
    for g in gens:
        g = np.array(g, dtype=complex)
        rows.append(np.kron(np.eye(n), g) - np.kron(g.T, np.eye(n)))
    a = np.vstack(rows)
    rank = np.linalg.matrix_rank(a, tol=TOL)
    return n * n - rank


def is_central(op: np.ndarray, gens, n: int) -> bool:
    """op is central in <gens>: op in the algebra AND commutes with every generator."""
    op = np.array(op, dtype=complex)
    for g in gens:
        g = np.array(g, dtype=complex)
        if np.linalg.norm(op @ g - g @ op) > TOL:
            return False
    basis = generated_algebra_basis(gens, n)
    a = np.array([b.reshape(-1) for b in basis]).T
    coef, *_ = np.linalg.lstsq(a, op.reshape(-1), rcond=None)
    return np.linalg.norm(a @ coef - op.reshape(-1)) < TOL


def _hermitian_basis(n: int):
    basis = []
    for i in range(n):
        e = np.zeros((n, n), dtype=complex)
        e[i, i] = 1.0
        basis.append(e)
    for i in range(n):
        for j in range(i + 1, n):
            e = np.zeros((n, n), dtype=complex)
            e[i, j] = 1.0
            e[j, i] = 1.0
            basis.append(e)
            e2 = np.zeros((n, n), dtype=complex)
            e2[i, j] = 1.0j
            e2[j, i] = -1.0j
            basis.append(e2)
    return basis


def metric_space(gens, n: int):
    """Hermitian solutions of Mostafazadeh's pseudo-Hermiticity intertwiner
    eta * g = g^dagger * eta for every generator g.

    Returns (dim, basis_metrics). This is metric SELECTION: an irreducible
    family pins dim == 1 (definite, unique up to scale); a reducible family
    leaves dim > 1 (a family that contains indefinite eta -> grading survives).
    """
    hbasis = _hermitian_basis(n)
    rows = []
    for g in gens:
        g = np.array(g, dtype=complex)
        gd = dagger(g)
        for h in hbasis:
            resid = h @ g - gd @ h  # linear in the coefficient of h
            rows.append(resid)
    # Build real constraint matrix over coefficients x (eta = sum x_k h_k).
    cmat = []
    for g in gens:
        g = np.array(g, dtype=complex)
        gd = dagger(g)
        blocks = [(h @ g - gd @ h).reshape(-1) for h in hbasis]
        m = np.array(blocks).T  # (n*n) x len(hbasis), complex
        cmat.append(np.vstack([m.real, m.imag]))
    c = np.vstack(cmat)
    # Null space of c (real).
    _, s, vh = np.linalg.svd(c)
    rank = int(np.sum(s > TOL))
    null = vh[rank:]
    metrics = []
    for row in null:
        eta = sum(coef * h for coef, h in zip(row, hbasis))
        metrics.append(eta)
    return len(metrics), metrics


def metric_is_uniquely_definite(gens, n: int):
    """(unique_definite, has_indefinite): does metric selection pin a single
    definite metric up to scale, and does the admissible family contain an
    indefinite metric (a surviving grading)?"""
    dim, metrics = metric_space(gens, n)
    if dim == 0:
        return False, False
    if dim == 1:
        eig = np.linalg.eigvalsh(metrics[0])
        definite = np.all(eig > TOL) or np.all(eig < -TOL)
        indefinite = (np.any(eig > TOL) and np.any(eig < -TOL))
        return bool(definite), bool(indefinite)
    # dim > 1 : sample the family for an indefinite representative.
    has_indef = False
    rng = np.random.default_rng(0)
    for _ in range(200):
        coefs = rng.normal(size=dim)
        eta = sum(c * m for c, m in zip(coefs, metrics))
        eig = np.linalg.eigvalsh(eta)
        if np.any(eig > TOL) and np.any(eig < -TOL):
            has_indef = True
            break
    return False, has_indef  # dim>1 => never uniquely definite


def recovery_exists(algebra_basis, line_from, line_to, n: int) -> bool:
    """Does the algebra contain m with m*line_from a nonzero vector on line_to?
    Equivalent: line_to in the subspace (algebra . line_from). Pure operational
    recoverability -- no metric, no grading, no ghost-parity semantics used."""
    v = line_from.reshape(n, 1)
    cols = [(a @ v).reshape(-1) for a in algebra_basis]
    w = np.array(cols).T
    target = line_to.reshape(-1)
    sol, *_ = np.linalg.lstsq(w, target, rcond=None)
    return np.linalg.norm(w @ sol - target) < TOL


