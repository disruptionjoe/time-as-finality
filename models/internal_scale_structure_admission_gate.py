def _anchor_checks() -> dict[str, Any]:
    t480 = run_t480()
    t480_minimum = t480["future_packet_minimum"]
    return {
        "t480_gate_passed": t480["overall_verdict"]["gate_passed"],
        "t480_external_or_internal_status_required": (
            "state whether the label is external bookkeeping or earned internal structure"
            in t480_minimum
        ),
        "t480_comparison_edges_required": (
            "state what observers and transport edges can compare after labeling"
            in t480_minimum
        ),
        "t480_no_clock_or_duration_earned": (
            t480["overall_verdict"]["record_clock_or_duration_earned"] is False
        ),
        "t480_no_scale_genesis_earned": (
            t480["overall_verdict"]["scale_genesis_theorem_earned"] is False
        ),
        "t480_positive_packet_bookkeeping_only": (
            t480["admitted_candidate_ids"] == ["declared_scale_section_bookkeeping"]
        ),
    }


def _evaluate_candidate(
    candidate: ScaleStructureCandidate,
    anchor_checks: dict[str, Any],
) -> dict[str, Any]:
    blockers: list[str] = []

    if not anchor_checks["t480_gate_passed"]:
        blockers.append("t480_gate_missing")
    if not candidate.declared_status:
        blockers.append("external_or_internal_status_not_declared")
    if not candidate.transported_structure:
        blockers.append("transported_structure_not_named")
    if not candidate.transport_law:
        blockers.append("transport_law_not_named")
    if not candidate.scale_label_operation:
        blockers.append("scale_label_operation_not_declared")
    if not candidate.comparison_domain:
        blockers.append("comparison_domain_not_declared")
    if candidate.label_only_without_operation:
        blockers.append("label_word_without_operation")
    if candidate.posthoc_comparison_domain:
        blockers.append("comparison_domain_selected_posthoc")
    if candidate.uses_hidden_calendar_or_time_order:
        blockers.append("hidden_calendar_or_time_order")
    if candidate.derives_duration_or_arrow_from_scale_structure:
        blockers.append("duration_or_arrow_derived_from_scale_structure")
    if candidate.changes_record_finality_by_scale_structure:
        blockers.append("record_finality_changed_by_scale_structure")
    if candidate.imports_rg_or_conformal_mechanism:
        blockers.append("rg_or_conformal_mechanism_imported")
    if candidate.claims_scale_genesis_or_physics:
        blockers.append("scale_genesis_or_physics_claim_overread")
    if candidate.requests_claim_or_posture_promotion:
        blockers.append("claim_or_public_posture_promotion_shortcut")
    if candidate.local_taf_anchor.startswith("external"):
        blockers.append("no_local_taf_anchor")

    if candidate.declares_earned_internal_structure:
        if not candidate.internal_generating_rule:
            blockers.append("internal_generating_rule_not_declared")
        if not candidate.positive_and_negative_controls_declared:
            blockers.append("positive_negative_controls_missing")
        if not candidate.relabel_invariance_check_declared:
            blockers.append("relabel_invariance_check_missing")
    elif not candidate.declares_external_bookkeeping:
        blockers.append("packet_status_not_external_or_internal")

    external_admitted = (
        not blockers
        and candidate.declares_external_bookkeeping
        and candidate.role == "positive_bookkeeping_control"
    )
    internal_review_admitted = (
        not blockers
        and candidate.declares_earned_internal_structure
        and candidate.role == "positive_review_control"
    )
    admitted = external_admitted or internal_review_admitted

    if external_admitted:
        route_label = "EXTERNAL_SCALE_BOOKKEEPING_ADMITTED_NO_PROMOTION"
        decision = "admit_external_bookkeeping"
    elif internal_review_admitted:
        route_label = "INTERNAL_SCALE_REVIEW_TARGET_ADMITTED_NO_PROMOTION"
        decision = "admit_synthetic_review_target"
    else:
        route_label = "SCALE_STRUCTURE_PACKET_BLOCKED"
        decision = "reject_or_block"

    return {
        "candidate_id": candidate.candidate_id,
        "role": candidate.role,
        "declared_status": candidate.declared_status,
        "transported_structure": candidate.transported_structure,
        "transport_law": candidate.transport_law,
        "scale_label_operation": candidate.scale_label_operation,
        "comparison_domain": candidate.comparison_domain,
        "internal_generating_rule": candidate.internal_generating_rule,
        "admitted_reading": candidate.admitted_reading,
        "local_taf_anchor": candidate.local_taf_anchor,
        "admitted": admitted,
        "decision": decision,
        "route_label": route_label,
        "blockers": blockers,
    }


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--write-results", action="store_true")
    args = parser.parse_args()

    result = run()
    if args.write_results:
        results_dir = Path("results")
        results_dir.mkdir(exist_ok=True)
        json_path = results_dir / f"{ARTIFACT_ID}.json"
        md_path = results_dir / f"{ARTIFACT_ID}-results.md"
        json_path.write_text(json.dumps(result, indent=2) + "\n", encoding="utf-8")
        md_path.write_text(render_markdown(result), encoding="utf-8")
    else:
        print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()
