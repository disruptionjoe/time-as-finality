            "| check | value |",
            "| --- | --- |",
            *anchor_rows,
            "",
            "## Candidate Evaluations",
            "",
            "| candidate | role | declared status | decision | route | blockers |",
            "| --- | --- | --- | --- | --- | --- |",
            *candidate_rows,
